class Solution{
public:	
	vector<int> prefix_function(string s) {
    int n = (int)s.length();
    vector<int> pi(n);
    for (int i = 1; i < n; i++) {
        int j = pi[i-1];
        while (j > 0 && s[i] != s[j])
            j = pi[j-1];
        if (s[i] == s[j])
            j++;
        pi[i] = j;
    }
    return pi;
}
	int isRepeat(string s)
	{
	    // Your code goes here
	    auto v = prefix_function(s);
	    int n = s.size();
	    int len = v[n-1];
	    if(n%(n-len)==0 and len>0)return 1;
	    return 0;
	}
};

