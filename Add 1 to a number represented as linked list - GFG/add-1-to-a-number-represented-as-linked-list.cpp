class Solution
{
    public:
    int dfs(Node * head ){
        if(!head){
            return 1;
        }
        int c = dfs(head -> next);
        int su = head -> data + c ;
        head -> data = su % 10;
        return su / 10;
    }
    Node* addOne(Node *head ) 
    {
        // Your Code here
        // return head of list after adding one
        int c = dfs(head);
        auto n = new Node(-1);
        if(c){
            n -> data = c;
            n -> next = head;
            return n;
        }
        return head;;
    }
};

