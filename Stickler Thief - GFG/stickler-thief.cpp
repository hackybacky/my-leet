class Solution
{
    public:
    //Function to find the maximum money the thief can get.
    int FindMaxSum(int arr[], int n)
    {
        // Your code here
        int p_not = 0 , p = 0;
        
        for(int i = 0 ;i < n ;i++){
            int cur = arr[i];
            int pcur = p_not + cur;
            int p_n = max(p , p_not);
            
            p = pcur;
            p_not = p_n;
        }
        return max(p_not , p);
    }
};

