class Solution {
  public:
    int maxBinTreeGCD(vector<vector<int>> arr, int N) {
        // code here
        
        map<int,int>child;
        int ans=0;
        for(auto it : arr){
            int p= it[0],c=it[1];
            
            if(child[p]){
                ans=max(__gcd(child[p],c),ans);
            }
            else child[p]=c;
        }
        return ans;
    }
};

